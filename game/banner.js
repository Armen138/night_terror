    colored: function () {
        let lines = banner.text.split("\n");
        let aligned = [];
        for(let line of lines) {
            if(line.length < 106) {
                line += " ".repeat(106 - line.length);
            }
            aligned.push(titles.header(line, banner.width));
        }
        return chalk.red(aligned.join('\n'));
    },
